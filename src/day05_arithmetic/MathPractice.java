package day05_arithmetic;

public class MathPractice {

    public static void main(String[] args) {

        System.out.println(1000 + 2000);
        System.out.println(2.5 + 3.5);

        System.out.println(100 - 30);
        int a = 300 - 100;
        System.out.println(a);

        System.out.println(20 * 6);
        System.out.println(5.5 * 7);

        System.out.println(10/4); //2 integer
        System.out.println(10.0/4); //2.5
        System.out.println(10/4.0); //2.5
        System.out.println(10.0/4.0); //2.5

        System.out.println(20/6); //3
        System.out.println(20D/6); //3.3333
        System.out.println(20/6D); //3.333 it also can be lowcase
        System.out.println("------------------------------");

        System.out.println(10 % 4); //2;   4+4=8   10-8=2
        System.out.println(20 % 5); //0 because there no left numbers


    }
}
