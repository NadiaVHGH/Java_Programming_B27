package day03_variables;

public class WorldCup {

    public static void main(String[] args) {

        int year = 2018;
        System.out.println(" The world cup " + year + " took place in Russia. ");
        year = 2022;
        System.out.println(" The world cup " + year + " will take place in Qatar. ");
        year = 2026;
        System.out.println(" The world cup " + year + " is being planned in North America. ");
    }
}
