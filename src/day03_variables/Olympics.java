package day03_variables;

public class Olympics {

    public static void main(String[] args) {

        int year = 2022;
        System.out.println(" The Olympic Games " + year + " were held in China.");
        year = 2024;
        System.out.println(" The Olympic Games " + year + " will be in France.");
        year = 2026;
        System.out.println(" The Olympic Games " + year + " will be in Italy.");

    }
}
