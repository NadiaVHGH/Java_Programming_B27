package day17_StringMethods7_11;

public class isEmpty {
    public static void main(String[] args) {
        String s = "hello";
        System.out.println(s.isEmpty());//false

        String s2 = " ";
        System.out.println(s2.isEmpty()); //false

        String s3 = "";
        System.out.println(s3.isEmpty()); //true


    }
}
